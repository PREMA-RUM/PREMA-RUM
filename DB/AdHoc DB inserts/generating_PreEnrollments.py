class PreEnrollmentSelectionGenerator:
    def __init__(self, student, preEnrollment_id, semesterOffers, preEnrollment):
        self.preEnrollmentId = preEnrollment_id
        self.semesterOffers = semesterOffers
        self.preEnrollmentTuple = preEnrollment

    def getSemesterOfferId(self, course):
        for semesterOffer in self.semesterOffers:
            if semesterOffer[1] == course:
                return semesterOffer[0]
        return -1

    def generateInsertValues(self):
        result = ''
        for course in self.preEnrollmentTuple:
            semester_Offer_id = self.getSemesterOfferId(course)
            if semester_Offer_id >= 0:
                result += ",(%s,%s)" % (
                    self.preEnrollmentId, self.getSemesterOfferId(course))
        return result


